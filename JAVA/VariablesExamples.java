public class VariablesExamples{
public static void main (String[] args)
{
byte byteVar = 100;
short shortVar = 2000;
int integerVar = -20;
long longVar = 1098765L;
float floatVar = 1.23f;
double doubleVar = 1.12345;
char charVar = 'V';
boolean booleanVar = true;

String stringVar = "Welcome !!";


System.out.println("Byte Value is:" + byteVar);
System.out.println("Short Value is:" + shortVar);
System.out.println("Integer Value is:" + integerVar);
System.out.println("Long Value is:" + longVar);
System.out.println("Float Value is:" + floatVar);
System.out.println("Double Value is:" + doubleVar);
System.out.println("Character Value is:" + charVar);
System.out.println("Boolean Value is:" + booleanVar);
System.out.println("String Value is:" + stringVar);
}
}




